
import java.util.*;

public class Linkedlist {
    public static void main(String[] args) {
        // creating linked list and object of the class
        LinkedList<String> ll = new LinkedList<String>();
        // Adding elements to the linked list
        ll.add("a");
        ll.add("b");
        ll.add("c");
        ll.add("d");
        ll.addFirst("e");
        System.out.println(ll);
    }

}
